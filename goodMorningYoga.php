        <h3 class="h3_kurse text-align_center margin-top_text"><span class="font-color-zuhause">7 Tipps für deinen perfekten Start in den Tag </span></h3>
        <p class="text_kurse">
            <br />
            <span class="font-color-zuhause">1. Starte den Morgen mit einem warmen Glas Wasser</span>
            <br>
            Wenn wir normal bekleidet sind, verlieren wir nachts bis zu 500 Milliliter Schweiß. 
            Trinke direkt nach dem Aufstehen ein großes Glas warmes Wasser, 
            um deinen Wasserhaushalt auszugleichen. Weitere Vorteile sind, dass du deinen Körper dabei unterstützt, 
            Giftstoffe auszuleiten und die Verdauung anzukurbeln. Ein halber Liter Wasser auf nüchternem Magen 
            soll die Durchblutung anregen und das Hautbild verbessern.   
             <br>
             <br>
             <span class="font-color-zuhause">2. Frische Luft!</span> 
            <br>
            Dafür musst du keinen Spaziergang machen. 
            Öffne dein Fenster und nimm dir erst einmal ganz bewusst ein paar tiefe Atemzüge,
             um neue Energie in deinen Körper fließen zu lassen.
            <br>
            <br>
            <span class="font-color-zuhause"> 3. Sei dankbar</span>
            <br>
            In Unternehmen, mit denen Shawn Achor zusammenarbeitete, 
            ließ er die Mitarbeiter 21 Tage lang täglich drei neue Dinge aufschreiben, 
            für die sie dankbar waren. Nach drei Wochen bildete sich ein neues Muster im Gehirn: 
            Die Menschen nahmen die Welt zuerst mit einer positiven statt einer negativen Einstellung wahr.
             Warum also nicht direkt den Morgen mit Dankbarkeit starten? Lege dir dafür ein Notizbuch an, 
             schreibe jeden Tag drei Dinge auf und beobachte, wie sich deine Einstellung mit der Zeit verändert.
             <br>
             <br>
             <span class="font-color-zuhause">4. Schreibe Tagebuch</span>
            <br>
            Es gibt Nächte, nach denen wir aufwachen und uns total gerädert fühlen. 
            In ihrem Buch “Der Weg des Künstlers” von Julia Cameron erklärt sie,
             dass es ein absolutes Muss ist, jeden Morgen drei leere Seiten Papier voll zu schreiben,
              um unsere Kreativität zu entfachen, da wir mit dieser Methode unseren Kopf von allen umher 
              schwirrenden Gedanken entladen. Schreibe also alles auf, was dir in den Sinn kommt, ohne darüber nachzudenken. 
              Auch wenn auf der Seite steht: “Ich weiß nicht, was ich schreiben soll. Die Übung nervt mich.” etc. 
              Auch Shawn Achor berichtet von positiven Auswirkungen des Journalings.
             <br>
             <br>
             <span class="font-color-zuhause">5. Yoga Asanas statt Kaffee</span>
            <br>
            Natürlich musst du deinen Kaffee am Morgen nicht weglassen, 
            aber: Für viele von uns ist der Gang zur Kaffeemaschine der erste am Morgen,
             weil wir müde sind. Das liegt daran, dass unsere Knochen, Sehnen und Bänder von der Nacht 
             noch ganz verschlafen sind. Mit sanften Yoga Flows weckst du deinen ganzen Körper auf –
              er wird dir dafür sehr dankbar sein. Starte mit unserem Guten Morgen Kurs online perfekt in deinen Tag.
            <br>
            <br>
             
            <span class="font-color-zuhause">6. Meditation</span>
            <br>
            Die Weltgesundheitsorganisatoin (WHO) beschreibt Stress als Epidemie des 21. Jahrhunderts. 
            Zahlreiche klinische Studien belegen mittlerweile, dass Meditation wirkt: Regelmäßiges 
            Meditieren beruhigt den Geist, fördert die Konzentration und wir können unseren Fokus wieder 
            auf das Wesentliche richten und den Stress reduzieren! Nutze gerne die 5-Minuten-Meditation “Innehalten” 
            von unserem Meditationslehrer und Coach Karl Semelka, um dir eine kurze Auszeit zu gönnen.
            <br>
            <br>
            <span class="font-color-zuhause">7. Lächle!</span>
            <br>
            80 Muskeln setzt der Mensch ein, wenn wir Lächeln. 
            Trotzdem entspannt sich unser Gesicht dabei. Indem wir lachen, 
            steigern wir unsere Immunabwehr, wir bauen Stresshormone ab, unsere Lungenfunktion verbessert sich 
            und unser Gehirn wird mit Endorphinen (Glückshormonen) versorgt. Und die nehmen wir doch immer gerne!
        </p>
